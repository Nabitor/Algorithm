# -*- coding=utf-8 -*-
class Solution(object):
    def twoSum(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        d = {}
        for i, n in enumerate(nums):
            m = target - n
            if m in d:
                return [d[m], i]
            else:
                d[n] = i


if __name__ == '__main__':
    solution = Solution()
    print(solution.twoSum([2, 7, 8, 9], 9))
